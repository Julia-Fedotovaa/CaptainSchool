from apps.education.models import (
    Track, Course, CourseCategory,
    Lesson, LessonCard, CardChoice,
)

def theory(lesson, order, title, content):
    return LessonCard.objects.create(
        lesson=lesson, order=order, card_type="theory",
        title=title, content=content,
    )

def test(lesson, order, question, choices):
    card = LessonCard.objects.create(
        lesson=lesson, order=order, card_type="test", content=question,
    )
    for i, (text, correct) in enumerate(choices, 1):
        CardChoice.objects.create(card=card, order=i, text=text, is_correct=correct)
    return card

def cat(name):
    obj, _ = CourseCategory.objects.get_or_create(name=name)
    return obj

def track(name, desc, fmt="Онлайн", diff="Начальный"):
    obj, _ = Track.objects.get_or_create(name=name, defaults={"description": desc, "format": fmt, "difficulty": diff})
    return obj

